//Crie uma função que dado dois valores (passados como parâmetros) mostre no console a soma, subtração,
//multiplicação e divisão desses valores

function contas(n1,n2){
    console.log(`A soma dos dois numeros é ${n1 + n2}`)
    console.log(`A subtração dos dois numeros é ${n1 - n2}`)
    console.log(`A divisão dos dois números é ${n1 / n2}`)
    console.log(`A multiplicação dos dois numéros é ${n1 * n2}`)
}

contas(10,5)